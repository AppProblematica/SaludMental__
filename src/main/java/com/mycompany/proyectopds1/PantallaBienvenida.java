    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JOptionPane.showMessageDialog(null, "Bienvenido al sistema de programación de actividades. Seleccione una opción:");

        String[] opciones = {"Semanales", "A largo plazo", "Mostrar actividades"};
        int opcion = JOptionPane.showOptionDialog(null, "Seleccione una opción:", "Sistema de Programación de Actividades", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, opciones, opciones[0]);

        if (opcion == 0) { // Opción Semanales
            int n = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la cantidad de actividades semanales:"));

            for (int i = 0; i < n; i++) {
                String actividad = JOptionPane.showInputDialog("Ingrese el nombre de la actividad " + (i + 1) + ":");
                actividadesCortoPlazo[numActividadesCortoPlazo] = actividad;
                int horas = Integer.parseInt(JOptionPane.showInputDialog("Ingrese las horas semanales dedicadas a " + actividad + ":"));
                horasSemanales[numActividadesCortoPlazo] = horas;
                numActividadesCortoPlazo++;
            }
        } else if (opcion == 1) { // Opción A largo plazo
            int n = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la cantidad de actividades a largo plazo:"));

            for (int i = 0; i < n; i++) {
                String actividad = JOptionPane.showInputDialog("Ingrese el nombre de la actividad " + (i + 1) + ":");
                actividadesLargoPlazo[numActividadesLargoPlazo] = actividad;
                int mes = Integer.parseInt(JOptionPane.showInputDialog("Ingrese los meses dedicados a " + actividad + ":"));
                meses[numActividadesLargoPlazo] = mes;
                numActividadesLargoPlazo++;
            }
        }

        String actividadesSemanales = "Actividades semanales:\n";
        for (int i = 0; i < numActividadesCortoPlazo; i++) {
            actividadesSemanales += actividadesCortoPlazo[i] + ": " + horasSemanales[i] + " horas semanales\n";
        }

        String actividadesLargoPlazoStr = "Actividades a largo plazo:\n";
        for (int i = 0; i < numActividadesLargoPlazo; i++) {
            actividadesLargoPlazoStr += actividadesLargoPlazo[i] + ": " + meses[i] + " meses\n";
        }

        JOptionPane.showMessageDialog(null, actividadesSemanales);
        JOptionPane.showMessageDialog(null, actividadesLargoPlazoStr);
    }//GEN-LAST:event_jButton3ActionPerformed

