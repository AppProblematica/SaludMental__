    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String[] preguntas = {
            "¿Poco interés o placer por hacer cosas?",
            "¿Se siente mal, deprimido o desesperanzado?",
            "¿Tiene problemas para conciliar o mantener el sueño, o duerme demasiado?",
            "¿Se siente cansado o con poca energía?",
            "¿Tiene poco apetito o come en exceso?",
            "¿Se siente mal consigo mismo, o cree que es un fracasado o que se ha defraudado a sí mismo o a su familia?",
            "¿Problemas para concentrarse en cosas, como leer el periódico o ver la televisión?",
            "¿Se mueve o habla tan despacio que los demás podrían haberlo notado? ¿O tan inquieto o inquieta que se ha movido mucho más de lo habitual?",
            "¿Tines pensamientos de que estarías mejor muerto, o pensamientos de hacerte daño de alguna manera?"
        };

        int resultado = 0;

        for (int i = 0; i < preguntas.length; i++) {
            String pregunta = preguntas[i] + "\n" +
                    "1: No en absoluto\n" +
                    "2: En varios días\n" +
                    "3: Más de la mitad de los días\n" +
                    "4: Casi todos los días";

            String[] opciones = {"No en absoluto", "En varios días", "Más de la mitad de los días", "Casi todos los días"};
            int opcion = JOptionPane.showOptionDialog(null, pregunta, "Diagnóstico de Depresión", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, opciones, opciones[0]);

            switch (opcion) {
                case 0:
                    resultado += 0;
                    break;
                case 1:
                    resultado += 1;
                    break;
                case 2:
                    resultado += 2;
                    break;
                case 3:
                    resultado += 3;
                    break;
            }
        }

        String diagnostico = "";
        if (resultado >= 0 && resultado <= 4) {
            diagnostico = "Depresión mínima o nula; Vigilar, puede no requerir tratamiento.";
        } else if (resultado >= 5 && resultado <= 9) {
            diagnostico = "Depresión leve; Utilizar el juicio clínico (duración de los síntomas, deterioro funcional) para determinar la necesidad de tratamiento.";
        } else if (resultado >= 10 && resultado <= 14) {
            diagnostico = "Depresión moderada; Utilizar el juicio clínico (duración de los síntomas, deterioro funcional) para determinar la necesidad de tratamiento.";
        } else if (resultado >= 15 && resultado <= 19) {
            diagnostico = "Depresión moderadamente grave; Garantiza un tratamiento activo con psicoterapia, medicación o una combinación de ambos.";
        } else if (resultado >= 20 && resultado <= 27) {
            diagnostico = "Depresión grave; Garantiza un tratamiento activo con psicoterapia, medicación o una combinación de ambos.";
        }

        JOptionPane.showMessageDialog(null, diagnostico);
    }//GEN-LAST:event_jButton5ActionPerformed

